import { Router } from 'express';
import { SwapController } from '../controllers/swapController';
import { authMiddleware } from '../middleware/auth';

const router = Router();

// Get swap quote (can be public, but we keep auth to align with user context)
router.post('/quote', authMiddleware, SwapController.getQuote);
router.post('/quote/simple', authMiddleware, SwapController.getQuoteSimple);

// Execute swap (requires auth)
router.post('/execute', authMiddleware, SwapController.execute);
router.post('/execute/simple', authMiddleware, SwapController.executeSimple);

// Cross-chain swap endpoints
router.post('/cross-chain/quote', authMiddleware, SwapController.getCrossChainQuote);
router.post('/cross-chain/execute', authMiddleware, SwapController.executeCrossChainSwap);
router.get('/cross-chain/status/:transactionId', authMiddleware, SwapController.getCrossChainStatus);
router.get('/supported-chains', SwapController.getSupportedChains);

export default router;
